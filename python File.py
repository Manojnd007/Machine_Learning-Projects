# Function definition
def add_numbers(a, b):
    """
    Function to add two numbers.

    Parameters:
    - a: First number
    - b: Second number

    Returns:
    Sum of a and b
    """
    return a + b

# Calling the function
num1 = 5
num2 = 7

result = add_numbers(num1, num2)

# Displaying the result
print(f"The sum of {num1} and {num2} is: {result}")
